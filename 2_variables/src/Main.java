public class Main {
    public static void main(String[] args) {

        // initialization
        int x = 123;
        double y = 3.14;
        boolean z = true;
        char symbol = '@';
        String name = "Adam";

        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("z = " + z);
        System.out.println("symbol = " + symbol);
        System.out.println("Hello " + name);
    }
}
